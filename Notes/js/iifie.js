/**
 * IIFE ('iffy')
 * Immediately invoted function expression
 * 
 * Immediately = right away
 * Invoked = run
 * Function  = ...function...
 * Expression = ...expression...
 * 
 * A function that we write and run at the same time
 */

(function () {
    window.addEventListener('load', function () {
        console.log('page is loaded')
    });
}());